package FujiShop.Entity;

public class Menus {
		private int id;
		private String ten_mn;
		private String url;
		public Menus() {
			super();
		}
		public int getId() {
			return id;
		}
		public void setId(int id) {
			this.id = id;
		}
		public String getTen_mn() {
			return ten_mn;
		}
		public void setTen_mn(String ten_mn) {
			this.ten_mn = ten_mn;
		}
		public String getUrl() {
			return url;
		}
		public void setUrl(String url) {
			this.url = url;
		}
		

}
